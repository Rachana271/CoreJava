package prog;

public class ForEachLoop {
	public static void main(String args[])
			{
			
			int s[] = {18,25,28,29,30};
			for (int i : s)
			{
				int a=i;
			System.out.print(a);
			System.out.print(" ");
			}
			System.out.println();
			
			char ch[] = {'R','A','C','H','A','N','A'};
			for (char ch1 : ch)
			{
				char a=ch1;
			System.out.print(a);
			System.out.print(" ");
			}
			}
	}


